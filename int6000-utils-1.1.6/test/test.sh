#!/bin/sh

# ====================================================================
# arg1 - command
# --------------------------------------------------------------------

execute()
{
	display
	display "\"${1}\""
	display "output:"
	${1} >> ${TESTLOG} 2>&1
	display "return: ${?}"
	display
}

# ====================================================================
# all args go to echo
# --------------------------------------------------------------------

display()

{
	echo "${@}" >> ${TESTLOG}
}

